export function imageVariationPrompt(prompt: string, index: number, total: number, hasReferences: boolean) {
    if (total <= 1) return prompt;

    return `${prompt}

多图生成要求 ${index + 1}/${total}：
以用户原始提示词为最高优先级，不要改变原始提示词要求的图片类型、用途、背景方向、主体关系、禁止项和商业目标。
本张只需要在不违背原始提示词的前提下，与其他结果形成可见差异，例如构图比例、镜头距离、拍摄角度、光影层次、产品位置、道具细节或背景细节可以有所不同。
如果原始提示词要求白底主图，就保持白底主图；如果原始提示词要求副图、场景图、详情图或其他类型，就保持对应类型。不要把原始需求改成另一种图片。
${hasReferences ? "严格保留参考产品识别：品牌标识、产品形状、包装比例、标签位置、配色、瓶盖形状、配件类型和核心视觉层级。不要把包装重设计成另一个产品。" : "生成明显不同的专业构图，不要重复同一个镜头角度、背景或产品摆放。"}
质量约束：真实商业摄影质感，边缘清晰，标签区域看起来清楚可信，光线干净，阴影自然，不要额外文字，不要水印，不要扭曲 logo，不要虚构包装，不要产品变形，不要杂乱道具。`;
}
