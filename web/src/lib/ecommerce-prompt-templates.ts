export function buildTemplatePrompt(template: EcommercePromptTemplate, currentPrompt?: string) {
    const current = currentPrompt?.trim();
    const base = `${amazonDesignerRole}\n\n工作流：${template.label}\n${template.prompt}\n\n${ecommerceQualityRules}`;
    if (!current) return base;
    return `${base}\n\n本次具体需求：${current}`;
}
